/**
 * Get total verses in a Juz
 */
export function getTotalVersesInJuz(juz) {
  const mapping = JUZ_MAPPING[juz];
  if (!mapping) return 0;
  return mapping.reduce((sum, range) => sum + (range.to - range.from + 1), 0);
}

/**
 * Get surah info by ID
 */
export function getSurahInfo(surahId) {
  return SURAH_INFO.find(s => s.id === surahId);
}
